let a = 10;
let b = 5;

// Equal to (==)
console.log("a == b:", a == b);  // Output: false

// Not equal to (!=)
console.log("a != b:", a != b);  // Output: true

// Greater than (>)
console.log("a > b:", a > b);    // Output: true

// Less than (<)
console.log("a < b:", a < b);    // Output: false

// Greater than or equal to (>=)
console.log("a >= b:", a >= b);  // Output: true

// Less than or equal to (<=)
console.log("a <= b:", a <= b);  // Output: false