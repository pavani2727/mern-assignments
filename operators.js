let num1 = 10;
let num2 = 3;

// Addition
let additionResult = num1 + num2;
console.log("Addition:", additionResult);  // Output: 13

// Subtraction
let subtractionResult = num1 - num2;
console.log("Subtraction:", subtractionResult);  // Output: 7

// Multiplication
let multiplicationResult = num1 * num2;
console.log("Multiplication:", multiplicationResult);  // Output: 30

// Division
let divisionResult = num1 / num2;
console.log("Division:", divisionResult);  // Output: 3.3333333333333335

// Modulus (remainder)
let modulusResult = num1 % num2;
console.log("Modulus:", modulusResult);  // Output: 1

// Exponentiation
let exponentiationResult = num1 ** num2;
console.log("Exponentiation:", exponentiationResult);  // Output: 1000
